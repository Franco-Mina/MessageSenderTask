package ar.edu.ubp.das.credenciales;

public class CredencialesBean {	
	private String cadenaConexion;
	private String usuario;
	private String password;       
	private String logPath;        
	private String pathConexiones;
	public String getCadenaConexion() {
		return cadenaConexion;
	}
	public void setCadenaConexion(String cadenaConexion) {
		this.cadenaConexion = cadenaConexion;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getLogPath() {
		return logPath;
	}
	public void setLogPath(String logPath) {
		this.logPath = logPath;
	}
	public String getPathConexiones() {
		return pathConexiones;
	}
	public void setPathConexiones(String pathConexiones) {
		this.pathConexiones = pathConexiones;
	}
}

                                              